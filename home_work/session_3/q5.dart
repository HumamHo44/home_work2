void main() {
  // a) Declare two integers a and b.
  int a = 20;
  int b = 40;

  //  b) Print outcomes of comparison operators: a == b, a != b, a > b, a < b, a >= b, a <= b.
  print(a == b);
  print(a != b);
  print(a > b);
  print(a < b);
  print(a >= b);
  print(a <= b);

  // c) Declare int sum = a + b; check if sum equals 20 and print the boolean result.
